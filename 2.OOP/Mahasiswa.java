public class Mahasiswa {
	private String nama;
	private float IPK;
	private int tanggalLahir;
	
	public String getNama() { return nama; }
	public void setNama(String newNama) { nama = newNama; }
	
	public float getIPK() { return IPK; }
	public void setIPK(float newIPK) { IPK = newIPK; }
	
	public int getTanggalLahir() { return tanggalLahir; }
	public void setTanggalLahir(int newTanggalLahir) { tanggalLahir = newTanggalLahir; }
}